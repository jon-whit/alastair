function [ ] = FPT_finite_gen( B,M,G,q1,r1,Prange )
%UNTITLED Summary of this function goes here
%   Detailed explanation goes here

syms b m g q r

A1_1 = sym(0);
A1_2 = sym(1);
A2_1 = sym(0);
A2_2 = sym(-b/m);
B1_1 = sym(0);
B2_1 = sym(1/m);
Q1_1 = sym(0);
Q1_2 = sym(0);
Q2_1 = sym(0);
Q2_2 = sym(q);
R1 = sym(r);

% constant definition
C = {'b',sym(B);...
     'm',sym(M);...
     'g',sym(G);...
     'q',sym(q1);...
     'r',sym(r1);...
     'A1_1',A1_1;...
     'A1_2',A1_2;...
     'A2_1',A2_1;...
     'A2_2',A2_2;...
     'B1_1',B1_1;...
     'B2_1',B2_1;...
     'Q1_1',Q1_1;...
     'Q1_2',Q1_2;...
     'Q2_1',Q2_1;...
     'Q2_2',Q2_2;...
     'R1',R1};
 
% variable definition
f32 = 'float32';
f64 = 'float64';


V = {f64,'P1_1',Prange(1,:);...
     f64,'P1_2',Prange(2,:);...
     f64,'P2_1',Prange(3,:);...
     f64,'P2_2',Prange(4,:)};

% Declaring definitions
A = sym('A',[2 2]);
B = sym('B',[2 1]);
Q = sym('Q',size(A));
R = sym('R',size(B,2));
P = sym('P',size(A));

Pnext =  Q + (A.'*P*A) - (A.'*P*B)*((R+B.'*P*B)^-1)*(B.'*P*A);
Knext = -1*((R+B.'*P*B)^-1)*(B.'*P*A);

r32 = 'rnd32';
r64 = 'rnd64';

D = {'Pnext_1_1',r32,Pnext(1,1);...
     'Pnext_1_2',r32,Pnext(1,2);...
     'Pnext_2_1',r32,Pnext(2,1);...
     'Pnext_2_2',r32,Pnext(2,2);...
     'Knext_1',r32,Knext(1);...
     'Knext_2',r32,Knext(2)};
 
% Declare Expressions

E = {'Pnext_1_1';...
     'Pnext_1_2';...
     'Pnext_2_1';...
     'Pnext_2_2';...   
     'Knext_1';...
     'Knext_2'...
     };
 
FPTaylor_file(C,V,D,E)

end

